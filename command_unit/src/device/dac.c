int     dac_open
    (
     int dac_index      /*!< [in] индекс цапа 0-3 */
     )
{
	/* конфигурируем интерфейсы ЦАПов */
	if (dac_index & DAC_01)
	{
		ad9116_open(0);		
	}
	if (dac_index & DAC_23)
	{
		ad9116_open(1);		
	}
	if (dac_index & DAC_45)
	{
		ad9116_open(2);		
	}
	if (dac_index & DAC_67)
	{
		ad9116_open(3);		
	}
	
	/* конфигурируем шкалу цап, настраиваем */
	
    
	return OK;
}

